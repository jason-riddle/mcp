def run_all_tests():
    """Run all integration test functions."""
    test_functions = [
        test_complete_pipeline_integration,
        test_extractor_generator_integration,
        test_validator_integration,
        test_file_manager_integration,
        test_error_recovery_integration,
        test_dry_run_integration,
        test_performance_integration,
    ]

    passed = 0
    failed = 0

    for test_func in test_functions:
        try:
            test_func()
            passed += 1
        except Exception as e:
            print(f"✗ {test_func.__name__}: {e}")
            import traceback
            traceback.print_exc()
            failed += 1

    print(f"\nIntegration Test Results: {passed} passed, {failed} failed")
    return failed == 0


