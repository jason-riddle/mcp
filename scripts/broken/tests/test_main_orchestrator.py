def test_orchestrator_integration():
    """Test integration of all orchestrator components."""
    with tempfile.TemporaryDirectory() as temp_dir:
        temp_path = Path(temp_dir)
        create_test_java_files(temp_path)
        create_test_readme(temp_path)

        config = DocumentationConfig.from_project_root(temp_path)
        orchestrator = DocumentationOrchestrator(config)

        # Test the complete workflow
        # 1. Extract data
        data = orchestrator._extract_data()
        assert len(data['tools']) > 0

        # 2. Generate content
        content = orchestrator._generate_content(data)
        assert 'tools' in content
        assert len(content['tools']) > 0

        # 3. Validate content
        is_valid = orchestrator._validate_content(content)
        assert is_valid == True

        # 4. Preview changes
        orchestrator._preview_changes(content)

    print("✓ test_orchestrator_integration")


def run_all_tests():
    """Run all test functions."""
    test_functions = [
        test_orchestrator_initialization,
        test_extract_data,
        test_generate_content,
        test_validate_content,
        test_dry_run_pipeline,
        test_full_pipeline,
        test_update_single_section,
        test_invalid_section,
        test_orchestrator_error_handling,
        test_orchestrator_integration,
    ]

    passed = 0
    failed = 0

    for test_func in test_functions:
        try:
            test_func()
            passed += 1
        except Exception as e:
            print(f"✗ {test_func.__name__}: {e}")
            failed += 1

    print(f"\nTest Results: {passed} passed, {failed} failed")
    return failed == 0


if __name__ == "__main__":
    success = run_all_tests()
    sys.exit(0 if success else 1)
