def create_test_java_files(temp_dir: Path):
    """Create minimal test Java files."""
    src_dir = temp_dir / "src" / "main" / "java" / "com" / "jasonriddle" / "mcp"
    src_dir.mkdir(parents=True)

    # Create minimal tools file
    tools_file = src_dir / "McpMemoryTools.java"
    tools_file.write_text('''
public class McpMemoryTools {
    @Tool(name = "memory.test_tool", description = "A test tool")
    public String testTool() {
        return "test";
    }
}
''')

    # Create minimal resources file
    resources_file = src_dir / "McpMemoryResources.java"
    resources_file.write_text('''
public class McpMemoryResources {
    @Resource(uri = "memory://test")
    TextResourceContents testResource() {
        return null;
    }
}
''')

    return src_dir


